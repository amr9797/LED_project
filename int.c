/******************************************************************************
* \Syntax          : void IRQ_initialize (IRQ_config * IRq_arr)         
* \Description     : enable the number of interrupts the user entered 
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : IRq_arr the array of interrupts 
* \Parameters (out): None                                                      
* \Return value:   : None                                  
*******************************************************************************/
 void IRQ_initialize (const IRQ_ConfigType * IRq_arr)   
 {
	 unsigned char i ,IRQn ,  reg , bit ; 
	 NVIC_APINT_R |= pri_subpri << pri_subpri_shift  ;   /*initiazlize goup/subgroup*/ 
	 for(i=0 ; i<IRQ_num ;i++) /*enable all the chosen IRQs*/
	 {
		 IRQn = (IRq_arr[i]).IRQ ; 
		 reg =IRQn / 32 ; /*get the right register */
		 bit =IRQn % 32 ; /*get the righr bit offset inside the register*/ 
		 
		 set_bit (IRQ_enable_base[reg] ,bit) ; /*set enable*/
		 
		 IRQ_pri_base[IRQn] = (unsigned char)(((IRq_arr[i]).pri) << reg_shiftbits ) ; /*set priority*/
	 
	 }
	 
	 
 }


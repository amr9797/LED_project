/******************************************************************************
* \Syntax          : void Turn_LED_On (void)      
* \Description     : turn the led on                                     
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Reentrant                                             
* \Parameters (in) : None                     
* \Parameters (out): None                                                      
* \Return value:   : None                                 
*******************************************************************************/
void Turn_LED_On (void)
{
	Dio_WriteChannel(LED ,high );
}
/******************************************************************************
* \Syntax          : void Turn_LED_Off (void)      
* \Description     : turn the led off                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Reentrant                                             
* \Parameters (in) : None                     
* \Parameters (out): None                                                      
* \Return value:   : None                                 
*******************************************************************************/

void Turn_LED_Off (void)
{
	Dio_WriteChannel(LED ,low );
}

/******************************************************************************
* \Syntax          : LED_State_type LED_State (void)      
* \Description     : read the led status                                     
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Reentrant                                             
* \Parameters (in) : None                     
* \Parameters (out): None                                                      
* \Return value:   : LED_State_type  led_status                                 
*******************************************************************************/

LED_State_type LED_State (void)
{
	unsigned char state ; 
	state = Dio_ReadChannel(LED);
 return state ; 	
	
}
